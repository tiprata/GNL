#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*d;

	d = (char*)s;
	i = 0;
	if (n != 0)
	{
		while (i < n)
		{
			d[i] = '\0';
			i++;
		}
	}
}

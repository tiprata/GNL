char	*ft_strrev(char *str)
{
	char	a;
	int		i;
	int		j;
	char	k;

	i = 0;
	j = 0;
	while (str[i] != '\0')
		i++;
	a = i / 2;
	while (j < a)
	{
		k = str[i - (j + 1)];
		str[i - (j + 1)] = str[j];
		str[j] = k;
		j++;
	}
	return (str);
}
